// Current language
let currentLang = localStorage.getItem('language') || 'en';

// Function to change language
function changeLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);
    
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang][key]) {
            // Check if content contains HTML (like <br>)
            if (translations[lang][key].includes('<br>')) {
                element.innerHTML = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    // Update placeholders for form inputs
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });
    
    // Update active language button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });
    
    // Update page title
    if (lang === 'tr') {
        document.title = 'Varşova\'da Taksi Şoförü Olun - Caca Partner | Bolt, Uber, FreeNow';
    } else {
        document.title = 'Work as Taxi Driver Warsaw - Caca Partner | Bolt, Uber, FreeNow';
    }
}

